#include <unistd.h>

void		expand_str(char *s)
{
	while (*s == ' ' || *s == '\t')
		s++;
	while (*s != '\0')
	{
		while (*s != ' ' && *s != '\t' && *s != '\0')
			write(1, s++, 1);
		while (*s == ' ' || *s == '\t')
			s++;
		if (*s != '\0')
			write(1, "   ", 3);
	}
}

int			main(int ac, char *av[])
{
	if (ac == 2)
		expand_str(av[1]);
	write(1, "\n", 1);
}
